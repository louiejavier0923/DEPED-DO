						<section class="inno-info-many">
							<p>Classes in the pre-school are expanded with the support of the Local School Board to prepare Grade I pupils and Strengthen early childhood education necessary in laying a good educational foundation in basic education. Almost all pupils who underwent pre-school education develop good reading habit as shown by the students.</p>
						</section>
					</section>
