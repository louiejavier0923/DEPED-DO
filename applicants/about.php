					<h2>History</h2>
					<div class="line"></div>
					<section class="history-info">
						<p>The Schools Division of Quezon City was formally established separately and distinct from the Manila City Schools on July 25, 1950. It started with 20,593 elementary school children and 310 secondary school students, with 478 teachers manning both levels. The Division Office was then situated in the old City Hall along Highway 54 (now EDSA), in Cubao, Quezon City.</p>
						<p>The first Schools Division Superintendent, Mr. Pablo T. Reyes (1950-1959), supervised the new division consisting initially 8 public elementary schools, and the lone secondary school, Quezon City High School (1947). In four decades, the Division greatly expanded due to the steady population growth of Quezon City and the influx of home seekers from various parts of the country.</p>
						<p>In 1962, when Mr. Alfredo Andal became Schools Superintendent, the Division had 43 complete elementary schools and five secondary schools. The enrollment rose to 58,300 elementary pupils and 8,250 high school students.</p>
						<p>With the appointment of Mrs. Conmemoracion M. Conception to Superintendent in 1969, the Division had grown to 63 elementary schools with 92,490 pupils, and 16 secondary schools with 33,537 students.</p>
						<p>In 1975, the public school enrollment multiplied by 26.3% to 111,372 elementary pupils and 47,799 high school students. The integration of four former municipalities of Rizal province (Mandaluyong, Marikina, Pasig and San Juan) into the Quezon City Schools Division, also in 1975, brought the total number of public elementary schools to 139, and the high schools to 28.</p>
						<p>During the administration of Dr. Edna B. Azurin (1980-1989), there were a total of 142 public elementary schools. Dr. Azurin’s successor, Dr. Bienvenido A. Icasiano (1989-1997), saw in 1994 the separation of the integrated municipalities from the Quezon City mother division to form separate schools divisions.</p>
						<p>Years later, during the last year of the next Superintendent, Dr. Aquilina S. Rivas (1997-2002) Quezon City’s public enrollments were regaining their previous number, 237,552 in the elementary level and 119,510 in the secondary level.</p>
						<p>The seventh Schools Superintendent , Dr. Victoria Q. Fuentes (2002-2010), transferred the Division Office at the newly-completed four-storey edifice with a multiple purpose rooftop inside the San Francisco High School compound at Misamis St. BagoBantay, Quezon City. She had under her administration and supervision 97 public elementary and 46 public secondary schools in Quezon City.</p>
						<p>Upon the retirement of Dr. Fuentes in 2010, the Division was temporarily led by two assistant superintendents as Offices-In-Charge, first, Dr. Rowena A. Cacanindin, and then Dr. Corazon C. Rubio, who eventually got the appointment as Schools Division Superintendent by the President in August 2011.</p>
						<p>In S.Y. 2013–2014, Dr. Ponciano A. Menguito was assigned as the Schools Division Superintendent until his promotion to Director III, DepEd-NCR with Dr. Helen Grace V. Go as Officer-In-Charge. At present, the Schools Division Office is under the sterling leadership of our Schools Division Superintendent Dr. Elizabeth E. Quesada. From an initial population of 20,593 elementary pupils and 310 high school students in 1950, the Division of Quezon City now serves a big population of 258,201 in its elementary schools and 143,462 in high schools. The services of teachers in the national rolls, 7,243 elementary teachers and 5,853 high school teachers.</p>
