						<h3>A DAY IN SCHOOL (ADIS) SUPERVISORY MONITORING</h3>
						<section class="inno-info-many">
							<p>This is the application of Community partnership in working out improvementand maintenance of school at no cost of the government. Every year from May 17 to 27, the school gathers, parents, teachers, NGOs, the Barangay in improving the school environment by cleaning, conducting repairs,repainting and other work activities that will prepare the school for the 1st day of school for children. Massive community support is denied in improving the school and the workplace through the Brigada Eskwela.</p>
						</section>
					</section>
					<section class="inno-info">
						<h3>PREPARATION OF MOCK TESTS AND PARALLEL TEST</h3>
						<section class="inno-info-many">
							<p>To prepare our pupils and students with the yearly National Achievement Test, Division Supervisors prepare MOCK Test that exactly look like the National Achievement Test in content and in procedure to familiarize them with the instructional procedures and at the same time ways in undergoing the said test with less tension and ease on the part of those administering said test. Early preparation gives readiness and improve competency of students in undergoing the said test.Parallel test items are prepared by both Master teachers at the school level and by Division Supervisors in the areas of their concern in the subjects tested in the National Achievement test and practiced in these MOCK test for students practice test and to gauge what unmastered skills have to be given more emphasis in teaching.</p>
						</section>
					</section>
					<section class="inno-info">
						<h3>MONDAY FOCUS GROUP DISCUSSION (FGD'S)</h3>
						<section class="inno-info-many">
							<p>This is a small group meeting done by the Schools Division Superintendent every Monday right after the flag ceremony (9:00-11:00 am) with the ASDS, Division and District Supervisors and Section Chiefs to discuss monitoring activities done by Division Supervisors and plan immediate solutions to address problems observed by soliciting shared ideas in a brainstorming session or through systematic or direct approaches. This small group meeting is followed-up with school principal concerned in giving immediate solutions or validating findings done by the monitoring teams. Very rich ideas come out from this Monday small group FGD's.</p>
						</section>
					</section>
					<section class="inno-info">
						<h3>MAXIMIZE CONTACT TIMENG</h3>
						<section class="inno-info-many">
							<p>This is a policy instituted by DepEd designed to arrest the deteriorating quality of education in the country especially in the public schools. It seeks to increase engaged time and tasks in all subject areas and reducing those activities that take teachers and students away from the classroom. Teaching and learning is more focused and varied activities are done in a better engaged and managed learning time within the alloted schedule per subject area.</p>
						</section>
					</section>
					<section class="inno-info">
						<h3>MASSIVE TRAINING OF TEACHERS (IN BOTH ES AND HS)</h3>
						<section class="inno-info-many">
							<p>All teachers both in the Elementary and High School underwent training by subject area focused on the following:</p>
							<ul>
								<li>Reading Remediation</li>
								<li>Master of Unmastered Skills</li>
								<li>Development of the Higher Ordered Thinking Skills</li>
								<li>Preparation of Test Books and Instructional Materials</li>
								<li>Training of Non-Majors in special subjects like English, Science, Math especially to TLE and MAPEH Teachers</li>
							</ul>
							<p>This training is funded by the Local School Board and Pump Priming funds of President Gloria Macapagal Arroyo.</p>
						</section>
					</section>
					<section class="inno-info">
						<h3>REVISED TEACHER HIRING POLICY</h3>
						<section class="inno-info-many">
							<p>DepEd's hiring policy was raised to improve the quality of the public school teachers hired. Under the new hiring system, teacher applicants must not only pass the Licensure Examination for Teachers or (LET). In addition, teachers must also pass a written exam in English and conduct demonstration teaching and pass an interview done at the Division level. This screening helps identify teachers who have good potentials and those that should be weed out.</p>
						</section>
					</section>
					<section class="inno-info">
						<h3>TEACHER INDUCTION PROGRAM</h3>
						<section class="inno-info-many">
							<p>Newly hired teachers or those being new in the service undergoes a one week Induction Program(TIP) in How and What to teach, to prepare them in this work as teachers. Strategies in teaching subject areas are introduced by the facilitators handled by Division Supervisors and Master Teachers. The What is focused on the content of the subject areas. Proper relationship in developing a better learning environment - are also emphasized in this induction activities to prepare new teachers to the world of work inside the workplace.</p>
						</section>
					</section>
					<section class="inno-info">
						<h3>CHILD FRIENDLY SCHOOL SYSTEM</h3>
						<section class="inno-info-many">
							<p>Every school in the Division follows the Child Friendly School System as guide post of School Management,CFSS is under the UNICEF program aimed to improve the teaching-learning process following the 7 Goals of CFSS whch are follows:</p>
							<ul>
								<li>Encourage children's participation in the school and the community</li>
								<li>Enhance children's health Wellbeing</li>
								<li>Guarantee safe and protective spaces for children</li>
								<li>Encourage enrolment and completion</li>
								<li>Ensure children's high academic achievement and success</li>
								<li>Raise teachers's morale and motivation</li>
								<li>Mobilize community's support for education</li>
							</ul>
							<p>Student Tracking System or STS is also well in place that tracks down performance of pupils especially student at risks. Focus of the CFSS is the learner.</p>
						</section>
					</section>
