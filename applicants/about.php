						<h3>ENHANCEMENT OF READING REMEDIATION</h3>
						<section class="inno-info-many">
							<p>Reading Remediation is enhanced with reading tools like Phil-IRRI ready to measure reading capabilities of pupils from Grade I-VI. Pull out system or individualized remediation are done at the school and Division need. Some formal remediation activities are handled by civic organization like the Rotaries, Petron and Academe like Ateneo through their Tulong Dunong Outreach Program. The Local School Board also funds formal Remedial Reading Activities at the school level by giving Honoraria for Remedial Reading teachers.</p>
						</section>
					</section>
					<section class="inno-info">
						<h3>IMPROVED FEEDING AND FOOD FOR SCHOOL PROGRAM</h3>
						<section class="inno-info-many">
							<p>Each school has well in place feeding program to help malnourished children. DepEd has put in place a Food for the School Program where 1 kilo of rice is given to one family of children enrolled in Grade I and Grade II. This encourage children not be absent since they are to receive the one kilo of rice daily. Feeding Programs are also funded by the Local School Board and civic aggregates like Jaycees and Rotaries.</p>
						</section>
					</section>
